package com.examples;

public class FirstLearningClass {
    public static void main(String[] args) {
        Sum();
        Minus();
        System.out.println("Hello World");
    }

    public static void Sum() {
        int a = 20;
        int b = 30;
        int s;
        s = a + b;
        System.out.println(s);
    }

    public static void Minus() {
        int a = 20;
        int b = 30;
        int s;
        s = a - b;
        System.out.println(s);
    }
}
