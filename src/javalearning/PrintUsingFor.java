package javalearning;

public class PrintUsingFor {
	public static void main(String[] args)
	{
		PrintUsingFor();
	}
	public static void PrintUsingFor()
	{
	  for (int i=1;i<=5; i++)
	  {
		  for (int j=1;j<=i;j++)
		  {
			  System.out.print(j+" ");		 
		  }
		System.out.println("");
	  }
	 }
	
}
